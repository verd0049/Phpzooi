    <h1>Theorie</h1>

    <h3>Wat is motivatie? Volgens de informatie in Definitie bron 3 zijn er vier verschillende soorten motivatie. De
        eerste soort is intinsieke motivatie. Bij dit soort motivatie doet een persoon een taak puur omdat hij of zij het
        leuk vindt om te doen en als het klaar is er voldoening van krijgt. De tweede vorm van motivatie is identificatie.
        Dit betekent dat een persoon taken gaat uit voeren die hij of zij moet doen om bij een groep te kunnen horen,
        vooral omdat mensen zich vaak veiliger voelen in een groep. De derde vorm van motivatie is introjectie. Dit houdt
        in dat een persoon iets doet om zijn of haar status omhoog te houden. De vierde en laatste vorm van motivatie is
        extrinsieke motivatie. Dit is hetzelfde als de eerste vorm, maar iemand doet het niet per se omdat hij of zij het
        leuk vindt, maar meer omdat ze streven naar het doel. Motivatie kan dus van van alles komen. Iets simpels als eten
        gaan halen als je honger hebt tot heel hard je best doen op je werk zodat je niet ontslagen wordt.</h3>

    <h3>Volgens de Ted Talk van Dan Pink heeft motivatie niet echt iets te maken met wat je er uiteindelijk voor krijgt,
        maar meer dat een persoon zelf in de hand heeft hoe de opdracht uitgevoerd wordt. Je kunt mensen het best
        gemotiveerd krijgen door ze bij wijze van spreken gewoon hun gang te laten gaan. Dus niet vaste werktijden en
        strenge regels, maar er gewoon voor zorgen dat iets op de goede manier af komt op de manier waar jij je het
        gemakkelijkst bij voelt. Volgens Dan Pink heeft motivatie er mee te maken dat je pas echt gemotiveerd werkt als je
        iets snel af krijgt. Dit lukt meestal alleen als iemand het ook leuk vind om te doen en niet alleen de beloning
        het leukst vindt.</h3>

    <h3>In beide artikelen komt weer terug dat mensen gemotiveerder zijn als ze bezig zijn met iets wat ze leuk vinden,
        wat Dan Pink dus ook al zei. In het eerste artikel wordt ook weer gezegd dat intinsieke motivatie voor betere
        uiteindelijke resultaten zorgt dan extrinsieke motivatie. In het tweede artikel wordt ook herhaald wat Dan Pink
        zei over dat mensen onder durk slechter presteren. In het artikel staat dat mensen onder druk slechter presteren
        omdat ze gestresst raken.</h3>

    <h1>Prestatiemotivatie</h1>

    <h3>Je bent in redelijke mate intrinsiek gemotiveerd en in redelijke mate extrinsiek gemotiveerd. Dat je redelijk
        intrinsiek gemotiveerd bent betekent dat je het best leuk vindt om te studeren. Maar héél leuk vind je het nou ook
        weer niet. Dat je in redelijke mate extrinsiek gemotiveerd bent wil zeggen dat je er ook rekening mee houdt wat je
        met je studie kunt bereiken: een diploma, geld, status of de erkenning van de mensen waar je van houdt. Op zich is
        er niks mis met jouw motivatie. Wél zou je gelukkiger zijn als je studie wat beter zou passen bij je werkelijke
        interesses.</h3>

    <h3>Het klopt wel enigzins dat ik het niet heel leuk vind om te studeren, dat verschilt ook met waar ik aan werk.
        Zoiets als deze opdracht vind ik helemaal niks aan en doe ik dus ook niet graag, maar opdrachten voor bijvoorbeeld
        computer science of programming basics doe ik juist wel graag, vooral omdat ik het idee krijg dat ik daar ook echt
        iets nieuws van leer. Dat ik ook een beetje extrinsiek gemotiveerd ben klopt ook wel, want ik doe dit ook zodat ik
        een soort verzekering heb van een wat betere baan. Voor status doe ik het echt niet daar geef ik niks om, het is
        meer dat ik tegen mezelf kan zeggen dat ik iets bereikt heb. Het klopt ook niet helemaal dat ik er gelukkiger van
        zou worden, als ik een studie deed die wat meer bij mijn interesses past. Op het moment heb ik niet echt speciaal
        interesses en ik vind dit juist erg interessant, zeker omdat ik er nog niet zo wonderbaarlijk veel van af weet en
        steeds meer leer. </h3>

    <h1>Carriereankers</h1>

    <h3>De meeste punten gaf je aan de rol: Rijkdom (18 punten) Rijkdom als carrièreanker zegt iets over het belang dat
        iemand hecht aan materiële beloning. Geld verdienen is dan veelal de belangrijkste reden om te werken. Een hoog
        inkomen en meer verdienen is voor deze personen de drijfveer voor het maken van loopbaankeuzes. Een hoog of hoger
        inkomen is voor deze persoon een belangrijke graadmeter voor succes.</h3>

    <h3>Daarna gaf je de meeste punten aan: Normen en waarden (15 punten) De belangrijkste drijfveer van iemand met dit
        carrièreanker is een bijdrage leveren aan een groter geheel. Vanuit persoonlijke waarden en normen wil men 'de
        wereld verbeteren' of een zinvolle bijdrage leveren aan de samenleving. Vaak maakt men daaraan het eigen belang
        ondergeschikt. Voornaamste doel is het gevoel hebben dat men anderen helpt.</h3>

    <h3>Het klopt niet helemaal dat rijkdom een van de belangrijkste drijfveren is voor mij. Het is zeker een
        belangrijke, want ik wil wel gewoon rond kunnen komen en me geen zorgen hoeven te maken over geld tekort komen,
        maar ik kies liever een leuke baan over een goed betaalde baan. Het gedeelte over normen en waarden klopt ook wel
        maar is niet gelijk het belangrijkste, want zoals ik al eerder zei ik heb liever een baan waar ik me goed bij
        voel. Ik ben daar nu nog niet helemaal zeker over, maar ik weet wel al door mijn bijbaan dat ik graag mensen help,
        ik ben ook zeker tevreden als het lukt om een klant ook blij te maken en als het niet lukt ben ik ook vrij
        ontevreden. </h3>

    <h3>Het minst aantal punten gaf je voor: Macht (3 punten) Deze mensen hebben graag de touwtjes in handen. Macht en
        invloed zijn de belangrijkste drijfveren in hun loopbaan. Ze worden gedreven door het verlangen anderen, de
        omgeving of situatie te kunnen leiden of beïnvloeden. Bij loopbaankeuzes laat men zich leiden door de vraag of men
        er meer invloed door krijgt. Ze netwerken graag en zien het aangaan van vriendschap of het leggen van een contact
        vaak als een manier om extra invloed uit te oefenen.</h3>

    <h3>Dit klopt helemaal. Macht en status spelen bij mij compleet geen rol. Ik geef er niet zo veel om wat anderen van
        mij vinden en ik doe deze studie zeker niet om iets tegenover iemand te bewijzen.</h3>

